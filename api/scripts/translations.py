def translate(text, dictionary):
    """Look up a text in a dictionary, case-insensitive."""
    if not text:
        return text
    lower = text.lower().strip()
    return dictionary.get(lower, text)


def translate_text_block(text, *dicts):
    """Try to translate known terms within a block of text."""
    if not text:
        return text
    result = text
    for d in dicts:
        for en, es in d.items():
            # Case-insensitive replacement
            import re
            result = re.sub(re.escape(en), es, result, flags=re.IGNORECASE)
    return result
